def PhotonAttenuationEl(Element, Energy, Option):
    CompEdge = []
    u = 1.6605402e-24  # atomic mass unit (1/12 of the mass of C-12)
    barns = 1e24  # barns
    x = (energy).T
    Energy = np.log(Energy)
    if any([Option == 1, Option == 3, Option == 4]):
        y = (mac[Element - 1, :]).T  # mass attenuation coefficient
    elif Option == 2:
        y = (meac[Element - 1, :]).T  # mass energy absorption coefficient
    elif Option > 4:
        print("Error! Only four outputs are possible!")
        return ["Set the function inputs again"]
    if Element > 10:  # check if there are compton edges
        ed = edges[edges[:, 0] == Element, :]  # check if Element has edges
        nEdge = len(ed)  # number of compton edges for Element
        e = ed[:, 1]  # energy of compton edges
        d = 4 * np.spacing(
            e
        )  # computes the positive distance from abs(X) to the next larger in magnitude floating point number of the same precision as X.
        xx = [
            e - d,
            e + d,
        ]  # d is the offset to create 2 different energies for upper and lower end of the edge. xx are the energies.
        w = np.concatenate(
            [np.zeros(len(x)), np.ones(2 * nEdge)]
        )  # array to help keep track of edges
        if any([Option == 1, Option == 3, Option == 4]):
            yy = (ed[:, [2, 3]]).T  # mac values
        elif Option == 2:
            yy = (ed[:, [4, 5]]).T  # meac values
        x = np.append(np.log(x), np.log(xx))
        y = np.append(np.log(y), np.log(yy))
        ix = np.argsort(x)  # merge compton data with other data
        x = x[ix]
        y = y[ix]
        w = w[ix]  # array with '0' for grid points and '1' for compton edge points
        w = (
            np.convolve(w, [1, 1, 1], "same") > 0
        )  # neighbors of edges will be marked with '1' too
        b1 = extrap(
            Energy, x, y
        )  # extrap function is equivalent to the Matlab interp1 function with extrap ON.
        b2 = extrap(Energy, x, w)
        a = pchip_interpolate(x, y, Energy)  # log-log pchip interpolation
        #        a = extrap(Energy,x,y) # log-log linear interpolation
        v = b2
        b = b1
        a = (
            a * (1 - v) + b * v
        )  # smoothly merge curves using linear near edges and cubic otherwise
        CompEdge = np.column_stack(((np.arange(1, nEdge + 1)).T, np.flipud(ed)))
    else:
        a = pchip_interpolate(
            np.log(x), np.log(y), Energy
        )  # log-log pchip interpolation
    #        a = extrap(Energy,np.log(x),np.log(y)) # log-log linear interpolation

    if any([Option == 1, Option == 3, Option == 4]):
        MAttCoef = np.exp(a)
    elif Option == 2:
        EAbsCoef = np.exp(a)
    # Select the desired output
    if Option == 1:  # mass attenuation coefficient
        coef = MAttCoef
    elif Option == 2:  # mass energy absorption coefficient
        coef = EAbsCoef
    elif Option == 3:  # linear attenuation coefficient (only for elements)
        coef = MAttCoef * np.float(PropsEl[Element - 1, 1])  # mac * density
    elif Option == 4:  # cross section in barns/atom
        coef = MAttCoef * (
            barns * u * (Element / np.float(PropsEl[Element - 1, 0]))
        )  # mac * (barns*u*A) A = Z/(Z/A)
    return [coef]


