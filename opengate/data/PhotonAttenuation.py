def ChComposition(Mixture):
    # function to extract the fraction by weight of each element of the mixture
    ChComp = PropsMix[
        PropsMix[:, 2] == Mixture, 4
    ]  # string array containing the elemental composition
    contents = str(ChComp)
    w = re.findall(
        r"[-+]?\d*\.*\d+", contents
    )  # contains the fraction by weight of each element of the mixture
    s = " ".join(re.findall("[a-zA-Z]+", contents))
    El = s.split()
    return w, El


