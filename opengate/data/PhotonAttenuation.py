# Linear interpolation with extrapolation outside the boundaries.
def extrap(x, xp, yp):
    """np.interp function with linear extrapolation"""
    y = interp1d(xp, yp, kind="linear")(x)
    if isinstance(x, list):
        y[x < xp[0]] = yp[0] + (x[x < xp[0]] - xp[0]) * (yp[0] - yp[1]) / (
            xp[0] - xp[1]
        )
        y[x > xp[-1]] = yp[-1] + (x[x > xp[-1]] - xp[-1]) * (yp[-1] - yp[-2]) / (
            xp[-1] - xp[-2]
        )
    else:
        if x < xp[0]:
            y = yp[0] + (x - xp[0]) * (yp[0] - yp[1]) / (xp[0] - xp[1])
        elif x > xp[-1]:
            y = yp[-1] + (x - xp[-1]) * (yp[-1] - yp[-2]) / (xp[-1] - xp[-2])
    return y


